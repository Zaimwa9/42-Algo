#include "../includes/libftprintf.h"

int		ft_strichrn(const char *s, int c, int n)
{
	int i;

	i = 0;
	while ((s[i]) && i < n)
		i++;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
